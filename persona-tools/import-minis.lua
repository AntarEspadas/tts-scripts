function click()
    local bag = getObjectFromGUID("d071c3")
    for k, v in pairs(personae) do
        spawn(k, v, bag)
    end
end

function spawn(persona, name, bag)
    spawnObject({
        ['type'] = 'Custom_Assetbundle',
        ['callback_function'] = function(obj) spawned(obj, persona, name, bag) end,
        ['scale'] = {1, 1, 1}
    })
end

function spawned(obj, persona, name, bag)
    local url = 'file:///D:\\TTS\\Persona\\persona\\AssetBundles\\' .. persona .. '.unity3d'
    obj.setCustomObject({
        ['assetbundle'] = url,
        ['type'] = 1,
    })
    obj.setName(name)
    bag.putObject(obj)
    -- print(obj.setLock(true))
